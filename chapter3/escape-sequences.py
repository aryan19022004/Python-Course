#Escape Sequences
#Escape sequences are special characters that are used to represent certain characters or actions in a string.
#The most common escape sequences are:
#\n - New line
#\t - Tab
#\\ - Backslash
#\' - Single quote
#\" - Double quote
print("Hello\nWorld")  # Output: Hello
                       #         World
print("Hello\tWorld")  # Output: Hello    World
print("Hello\\World")  # Output: Hello\World
print("Hello\'World")  # Output: Hello'World
print("Hello\"World")  # Output: Hello"World    
